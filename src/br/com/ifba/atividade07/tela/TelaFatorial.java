    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        
        // pega o valor inserido no txtNumero e o converte para inteiro
        int n = Integer.parseInt(txtNumero.getValue().toString());
        
        // cria um objeto da classe Fatorial
        Fatorial f = new Fatorial();
        
        // calcula o fatorial de n
        f.setValor(n); 
        
        // mostra a formula do calculo do fatorial na interface
        lblFormula.setText(f.getFormula()); 
        
        // mostra o resultado do calculo do fatorial na interface
        lblFatorial.setText(Integer.toString(f.getFatorial()));
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

